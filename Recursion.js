const numbers = [
  [1, 2, 3, 4],
  [3, 6, [5, 6], 8, 2, [2, 4], 9],
  [4, 2, [6, 7, [2, 6, 1]]],
];

const flattenedArray = numbers.flat(3);

const sum = (flattenedArray) => {
  if (flattenedArray.length === 0) {
    return 0;
  } else {
    return flattenedArray[0] + sum(flattenedArray.slice(1));
  }
};

console.log(`The sum is ${sum(flattenedArray)}`);

